    def _execute_enable_api(self, action_arguments: Dict[str, Any]) -> bool:
        """Action: enable one API service if it is not already enabled."""
        api_service_name = action_arguments.get("service", "").strip()
        if not api_service_name:
            self._log("  ✗ enable_api requires args.service", "error")
            return False

        enabled_services = set(self._collect_enabled_api_services())
        if api_service_name in enabled_services:
            self._log(f"  ✓ API already enabled: {api_service_name}", "success")
            # Diagnostic info when debugging: include enabled-services snapshot
            if self.debug_level >= 2:
                self._last_action_artifacts = {"enabled_services": list(enabled_services)}
            return True

        try:
            self._log(f"  Enabling API: {api_service_name}", "info")
            self._run_command(["gcloud", "services", "enable", api_service_name])
            self._log(f"  ✓ API enabled: {api_service_name}", "success")
            if self.debug_level >= 2:
                enabled_after = self._collect_enabled_api_services()
                self._last_action_artifacts = {"enabled_services": enabled_after}
            return True
        except subprocess.CalledProcessError:
            return False

    def _execute_ensure_service_account(
        self,
        action_arguments: Dict[str, Any],
    ) -> bool:
        """Action: create service account only if it does not already exist."""
        payload = self._build_service_account_payload(action_arguments)
        service_account_name = payload["name"]
        service_account_email = payload["email"]

        if not service_account_name:
            self._log(
                "  ✗ ensure_service_account requires service_account_name",
                "error",
            )
            return False

        self._log(f"  Ensuring service account exists: {service_account_name}", "info")
        describe_result = self._run_command(
            [
                "gcloud",
                "iam",
                "service-accounts",
                "describe",
                service_account_email,
                "--format=json",
            ],
            check_return_code=False,
        )

        if describe_result.returncode == 0:
            # try to parse JSON diagnostic output when available
            try:
                parsed = (
                    json.loads(describe_result.stdout) if describe_result.stdout else {}
                )
                self._last_action_artifacts = {"service_account": parsed}
            except Exception:
                pass
            self._log(f"  ✓ Service account exists: {service_account_name}", "success")
            return True

        try:
            create_command = [
                "gcloud",
                "iam",
                "service-accounts",
                "create",
                service_account_name,
                f"--display-name={payload['display_name']}",
            ]
            if payload["description"]:
                create_command.append(f"--description={payload['description']}")

            self._run_command(create_command)
            self._log(f"  ✓ Service account created: {service_account_name}", "success")
            # fetch and include created SA details when debugging
            if self.debug_level >= 2:
                try:
                    info_res = self._run_command(
                        [
                            "gcloud",
                            "iam",
                            "service-accounts",
                            "describe",
                            service_account_email,
                            "--format=json",
                        ],
                        check_return_code=False,
                    )
                    if info_res.returncode == 0 and info_res.stdout:
                        self._last_action_artifacts = {
                            "service_account": json.loads(info_res.stdout)
                        }
                except Exception:
                    pass
            return True
        except subprocess.CalledProcessError:
            return False

    def _build_key_file_options(
        self,
        action_arguments: Dict[str, Any],
    ) -> Dict[str, Any]:
        """Resolve key creation options using defaults and action overrides."""
        default_credentials = self.defaults_config.get("credentials", {})

        resolved_key_directory = action_arguments.get(
            "key_path",
            default_credentials.get("key_path", "./credentials"),
        )
        resolved_key_file = action_arguments.get(
            "key_file",
            default_credentials.get("key_file", "service-account-key.json"),
        )

        return {
            "key_path": resolved_key_directory,
            "key_file": resolved_key_file,
            "overwrite_local_key": action_arguments.get(
                "overwrite_local_key",
                default_credentials.get("overwrite_local_key", False),
            ),
            "allow_new_key_creation_if_keys_exist": action_arguments.get(
                "allow_new_key_creation_if_keys_exist",
                default_credentials.get(
                    "allow_new_key_creation_if_keys_exist",
                    False,
                ),
            ),
            "delete_existing_user_managed_keys_before_create": action_arguments.get(
                "delete_existing_user_managed_keys_before_create",
                default_credentials.get(
                    "delete_existing_user_managed_keys_before_create",
                    False,
                ),
            ),
        }

    def _execute_ensure_service_account_key(
        self,
        action_arguments: Dict[str, Any],
    ) -> bool:
        """Action: create/reuse key based on safe duplicate-prevention options."""
        payload = self._build_service_account_payload(action_arguments)
        service_account_email = payload["email"]
        if not service_account_email:
            self._log(
                "  ✗ ensure_service_account_key requires service_account_name", "error"
            )
            return False

        key_options = self._build_key_file_options(action_arguments)
        key_output_path = (
            self._resolve_path(key_options["key_path"]) / key_options["key_file"]
        )
        key_output_path.parent.mkdir(parents=True, exist_ok=True)

        existing_remote_keys = self._collect_user_managed_service_account_keys(
            service_account_email,
        )

        if key_output_path.exists() and not key_options["overwrite_local_key"]:
            self._log(f"  ✓ Local key already exists: {key_output_path}", "success")
            self._log("  Skipping key creation (overwrite_local_key is false)", "info")
            return True

        if (
            existing_remote_keys
            and key_options["delete_existing_user_managed_keys_before_create"]
        ):
            self._log("  Deleting existing user-managed keys before create...", "info")
            try:
                for key_data in existing_remote_keys:
                    key_name = key_data.get("name", "")
                    if key_name:
                        self._delete_user_managed_service_account_key(
                            service_account_email,
                            key_name,
                        )
                self._log("  ✓ Deleted existing user-managed keys", "success")
                existing_remote_keys = []
            except subprocess.CalledProcessError as delete_error:
                self._log(f"  ✗ Failed deleting keys: {delete_error}", "error")
                return False

        if (
            existing_remote_keys
            and not key_options["allow_new_key_creation_if_keys_exist"]
        ):
            self._log("  ⚠ Existing user-managed keys already exist in GCP", "warning")
            self._log("  Skipping duplicate key creation", "warning")
            return True

        if key_output_path.exists() and key_options["overwrite_local_key"]:
            key_output_path.unlink()
            self._log(f"  ⚠ Removed local key file: {key_output_path}", "warning")

        try:
            self._run_command(
                [
                    "gcloud",
                    "iam",
                    "service-accounts",
                    "keys",
                    "create",
                    str(key_output_path),
                    f"--iam-account={service_account_email}",
                ]
            )
            self._log(f"  ✓ Service account key created: {key_output_path}", "success")
            # include local+remote key state in artifacts when debugging
            try:
                remote_keys = self._collect_user_managed_service_account_keys(
                    service_account_email
                )
            except Exception:
                remote_keys = []

            self._last_action_artifacts = {
                "created_files": [str(key_output_path)],
                "remote_keys": remote_keys,
            }
            return True
        except subprocess.CalledProcessError:
            return False

    def _execute_ensure_project_iam_binding(
        self,
        action_arguments: Dict[str, Any],
    ) -> bool:
        """Action: assign project IAM role only when the binding is missing."""
        role_name = action_arguments.get("role", "").strip()
        member_name = action_arguments.get("member", "").strip()

        if not member_name and action_arguments.get("service_account_name"):
            service_account_name = action_arguments["service_account_name"]
            member_name = (
                f"serviceAccount:{service_account_name}"
                f"@{self.project_id}.iam.gserviceaccount.com"
            )

        if not role_name or not member_name:
            self._log(
                "  ✗ ensure_project_iam_binding requires args.role and args.member",
                "error",
            )
            return False

        iam_policy = self._collect_project_iam_policy()
        if iam_policy and self._member_has_role_binding(
            iam_policy, member_name, role_name
        ):
            self._log(
                f"  ✓ IAM binding already exists: {member_name} -> {role_name}",
                "success",
            )
            if self.debug_level >= 2:
                self._last_action_artifacts = {"iam_policy": iam_policy}
            return True

        try:
            self._run_command(
                [
                    "gcloud",
                    "projects",
                    "add-iam-policy-binding",
                    self.project_id,
                    f"--member={member_name}",
                    f"--role={role_name}",
                    "--quiet",
                ]
            )
            # fetch updated policy for diagnostics
            try:
                iam_policy_after = self._collect_project_iam_policy()
            except Exception:
                iam_policy_after = {}

            if self.debug_level >= 2:
                self._last_action_artifacts = {"iam_policy": iam_policy_after}

            self._log(
                f"  ✓ IAM binding created: {member_name} -> {role_name}",
                "success",
            )
            return True
        except subprocess.CalledProcessError:
            return False

    def _persist_environment_variable(
        self,
        variable_name: str,
        variable_value: str,
    ) -> None:
        """Persist environment variable for future shell sessions when possible."""
        os.environ[variable_name] = variable_value
        self._log(f"  Persisting environment variable {variable_name}...", "info")

        if os.name == "nt":
            self._run_command(["setx", variable_name, variable_value])
            self._log(
                f"  ✓ Persisted {variable_name} for future Windows terminals",
                "success",
            )
            return

        self._log(
            "  ⚠ Auto persistence is only implemented for Windows.",
            "warning",
        )

    def _execute_configure_local_credentials(
        self,
        action_arguments: Dict[str, Any],
    ) -> bool:
        """Action: write env file and optional env persistence/activation."""
        payload = self._build_service_account_payload(action_arguments)
        service_account_email = payload["email"]
        if not service_account_email:
            self._log(
                "  ✗ configure_local_credentials requires service_account_name",
                "error",
            )
            return False

        default_credentials = self.defaults_config.get("credentials", {})
        key_path_value = action_arguments.get(
            "key_path",
            default_credentials.get("key_path", "./credentials"),
        )
        key_file_value = action_arguments.get(
            "key_file",
            default_credentials.get("key_file", "service-account-key.json"),
        )
        key_output_path = self._resolve_path(key_path_value) / key_file_value

        if not key_output_path.exists():
            self._log(
                "  ⚠ Local key file missing; skipping local credential configuration",
                "warning",
            )
            return True

        write_env_file = action_arguments.get(
            "write_env_file",
            default_credentials.get("write_env_file", True),
        )
        env_filename = action_arguments.get(
            "env_file",
            default_credentials.get("env_file", ".env.gcp"),
        )

        persist_google_application_credentials = action_arguments.get(
            "persist_google_application_credentials",
            default_credentials.get(
                "persist_google_application_credentials",
                False,
            ),
        )
        persist_google_cloud_project = action_arguments.get(
            "persist_google_cloud_project",
            default_credentials.get("persist_google_cloud_project", False),
        )
        activate_service_account_for_gcloud = action_arguments.get(
            "activate_service_account_for_gcloud",
            default_credentials.get("activate_service_account_for_gcloud", False),
        )

        try:
            if write_env_file:
                env_output_path = self._resolve_path(env_filename)
                env_lines = [
                    f"GOOGLE_CLOUD_PROJECT={self.project_id}",
                    f"GOOGLE_PROJECT_ID={self.project_id}",
                    f"GOOGLE_PROJECT_NUMBER={self.project_number}",
                    f"GOOGLE_APPLICATION_CREDENTIALS={key_output_path}",
                    f"GCP_SERVICE_ACCOUNT_EMAIL={service_account_email}",
                ]
                env_output_path.write_text("\n".join(env_lines) + "\n", encoding="utf-8")
                self._log(f"  ✓ Wrote environment file: {env_output_path}", "success")
                # Record artifact(s) for structured run log
                self._last_action_artifacts = {
                    "created_files": [str(env_output_path), str(key_output_path)]
                }

            if persist_google_application_credentials:
                self._persist_environment_variable(
                    "GOOGLE_APPLICATION_CREDENTIALS",
                    str(key_output_path),
                )

            if persist_google_cloud_project:
                self._persist_environment_variable(
                    "GOOGLE_CLOUD_PROJECT",
                    self.project_id,
                )

            if activate_service_account_for_gcloud:
                self._run_command(
                    [
                        "gcloud",
                        "auth",
                        "activate-service-account",
                        service_account_email,
                        f"--key-file={key_output_path}",
                    ]
                )
                self._log("  ✓ Activated service account for gcloud", "success")

            return True
        except (OSError, subprocess.CalledProcessError) as setup_error:
            self._log(f"  ✗ Failed local credential setup: {setup_error}", "error")
            return False

    def _execute_component(
        self,
        component_item: Dict[str, Any],
    ) -> bool:
        """Execute one component item using mapped action handler."""
        # Each component is a small declarative unit of work.
        component_identifier = component_item.get("id", "unnamed-component")
        action_name = component_item.get("action", "").strip()
        is_enabled = component_item.get("enabled", True)
        raw_arguments = component_item.get("args", {})

        if not is_enabled:
            self._log(f"- Skipping disabled component: {component_identifier}", "warning")
            return True

        if not action_name:
            self._log(f"- Component '{component_identifier}' missing action", "error")
            return False

        if not isinstance(raw_arguments, dict):
            self._log(
                f"- Component '{component_identifier}' args must be an object",
                "error",
            )
            return False

        action_handler = self.action_handlers.get(action_name)
        if not action_handler:
            self._log(
                f"- Unknown action '{action_name}' in component '{component_identifier}'",
                "error",
            )
            return False

        resolved_arguments = self._resolve_argument_templates(raw_arguments)
        self._log(
            f"\n→ Executing component: {component_identifier} ({action_name})",
            "info",
        )
        # Reset last action artifacts before running
        self._last_action_artifacts = None
        try:
            result = action_handler(resolved_arguments)
            message = None
        except Exception as exc:  # catch unexpected exceptions to record them
            result = False
            message = str(exc)
            self._log(f"  ✗ Exception while executing component: {message}", "error")

        artifacts = None
        if isinstance(self._last_action_artifacts, dict):
            artifacts = self._last_action_artifacts.get("created_files")

        self._append_run_log_entry(
            component_id=component_identifier,
            action=action_name,
            args=resolved_arguments,
            result=bool(result),
            message=message,
            artifacts=artifacts,
        )

        # Clear last action artifacts for next component
        self._last_action_artifacts = None
        return result

    def execute_components(self) -> bool:
        """Execute all configured components in order from components config."""
        # Ordering is intentional: later components may depend on earlier ones.
        if not self.components:
            self._log("⚠ No components configured; nothing to execute", "warning")
            return True

        for component_item in self.components:
            if not isinstance(component_item, dict):
                self._log("✗ Component entry must be an object", "error")
                return False

            if not self._execute_component(component_item):
                return False

        return True

    def run_setup(self) -> None:
        """Run setup lifecycle: preflight, auth/defaults, component execution."""
        project_label = self.project_name or "Unnamed Project"
        self._log("=" * 60, "info")
        self._log("Google Cloud Setup Automation", "info")
        self._log("=" * 60, "info")
        self._log(f"Project: {project_label} ({self.project_id})", "info")

        if self.execution_config.get("dry_run", False):
            self._log(
                "Running in dry-run mode. No remote changes will be applied.",
                "warning",
            )

        if not self.check_prerequisites():
            sys.exit(1)

        setup_steps: List[tuple[str, Callable[[], bool]]] = [
            ("Activating gcloud configuration", self.setup_gcloud_configuration),
            ("Ensuring gcloud authentication", self.ensure_authenticated_account),
            ("Setting up ADC", self.configure_application_default_credentials),
            ("Setting gcloud defaults", self.configure_gcloud_defaults),
            ("Executing component orchestration", self.execute_components),
        ]

        failed_steps: List[str] = []
        for step_name, step_function in setup_steps:
            try:
                if not step_function():
                    failed_steps.append(step_name)
            except Exception as unexpected_error:  # noqa: BLE001
                self._log(f"✗ Error during {step_name}: {unexpected_error}", "error")
                failed_steps.append(step_name)

        # Persist run log and manifest regardless of success/failure
        try:
            self._write_run_artifacts()
        except Exception:
            # _write_run_artifacts already logs any write errors; ignore here
            pass

        self._log("\n" + "=" * 60, "info")
        if failed_steps:
            self._log("✗ Setup completed with errors:", "error")
            for failed_step_name in failed_steps:
                self._log(f"  - {failed_step_name}", "error")
            sys.exit(1)

        self._log("✓ GCP setup completed successfully", "success")
        self._log("=" * 60, "info")


