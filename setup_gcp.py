#!/usr/bin/env python3
"""Automate Google Cloud setup using declarative orchestration config.

Overview
--------
This script executes Google Cloud setup tasks from YAML configuration rather
than hardcoded Python logic. The architecture separates environment/runtime
settings from actionable cloud resources:

- Runtime config file (default: ``gcp_config.yaml``)
- Components config file (default: ``gcp_components.yaml``)

Execution Model
---------------
1. Load defaults, runtime config, and CLI overrides.
2. Validate required project configuration.
3. Configure local gcloud profile and authentication.
4. Execute component actions in declared order.

Why This Script Exists
----------------------
- Keep setup portable across projects and accounts.
- Support repeatable/idempotent infrastructure bootstrapping.
- Let teams extend setup by editing YAML instead of Python code.

Quick Usage
-----------
- Preview only: ``python setup_gcp.py --dry-run``
- Execute setup: ``python setup_gcp.py``
- Custom files:
    ``python setup_gcp.py --config my_runtime.yaml``
    ``--components-config my_components.yaml``
 
Author: Cris Magalang (crismag)
GitHub: https://github.com/crismag/GoogleCloudPlatform_Utilities
Last Updated: 2026-03-23

Author: Cris Magalang (crismag)
"""

