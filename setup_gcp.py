def build_cli_override_dictionary(arguments: argparse.Namespace) -> Dict[str, Any]:
    """Convert parsed CLI values to nested runtime override dictionary."""
    override_dictionary: Dict[str, Any] = {
        "project": {},
        "gcloud": {},
        "defaults": {
            "service_account": {},
            "credentials": {},
        },
        "components_config": {},
        "execution": {},
    }

    argument_mappings = [
        ("project", "id", arguments.project_id),
        ("project", "name", arguments.project_name),
        ("project", "number", arguments.project_number),
        ("gcloud", "configuration_name", arguments.gcloud_configuration_name),
        ("gcloud", "account", arguments.gcloud_account),
        ("gcloud", "run_region", arguments.run_region),
        ("gcloud", "compute_region", arguments.compute_region),
        ("gcloud", "compute_zone", arguments.compute_zone),
    ]

    # debug level mapping
    if getattr(arguments, "debug_level", None) is not None:
        override_dictionary["execution"]["debug_level"] = int(arguments.debug_level)

    for section_name, key_name, argument_value in argument_mappings:
        if argument_value:
            override_dictionary[section_name][key_name] = argument_value

    if arguments.default_service_account_name:
        override_dictionary["defaults"]["service_account"][
            "name"
        ] = arguments.default_service_account_name

    if arguments.default_key_path:
        override_dictionary["defaults"]["credentials"][
            "key_path"
        ] = arguments.default_key_path

    if arguments.default_key_file:
        override_dictionary["defaults"]["credentials"][
            "key_file"
        ] = arguments.default_key_file

    if arguments.components_config:
        override_dictionary["components_config"]["path"] = arguments.components_config

    if arguments.dry_run:
        override_dictionary["execution"]["dry_run"] = True

    return _remove_empty_nested_values(override_dictionary)


def _remove_empty_nested_values(raw_dictionary: Dict[str, Any]) -> Dict[str, Any]:
    """Remove empty nested dictionaries/lists for predictable deep-merge behavior."""
    compact_dictionary: Dict[str, Any] = {}

    for key, value in raw_dictionary.items():
        if isinstance(value, dict):
            cleaned_value = _remove_empty_nested_values(value)
            if cleaned_value:
                compact_dictionary[key] = cleaned_value
            continue

        if isinstance(value, list):
            if value:
                compact_dictionary[key] = value
            continue

        if value not in (None, ""):
            compact_dictionary[key] = value

    return compact_dictionary


def main() -> None:
    """Parse CLI arguments and execute setup automation."""
    argument_parser = build_cli_argument_parser()
    parsed_arguments = argument_parser.parse_args()

    cli_override_dictionary = build_cli_override_dictionary(parsed_arguments)
    setup_automation = GCloudSetupAutomation(
        runtime_config_path=parsed_arguments.config,
        cli_override_dictionary=cli_override_dictionary,
    )
    setup_automation.run_setup()


if __name__ == "__main__":
    main()
